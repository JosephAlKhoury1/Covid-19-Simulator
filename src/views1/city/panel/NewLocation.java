    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        try {
            String sQ = quantityTxt.getText().trim();
            this.quantity = Integer.parseInt(sQ);
        } catch (NumberFormatException e) {
            JOptionPane.showOptionDialog(this.dialog, Messages.createNewLocationQuantityMusstBeNumber(), Messages.error(), JOptionPane.CLOSED_OPTION, JOptionPane.ERROR_MESSAGE, null, null, null);
            return;
        }
        if (this.quantity == 0) {
            JOptionPane.showOptionDialog(this.dialog, Messages.createNewLocationQuantityCantbeNull(), Messages.error(), JOptionPane.CLOSED_OPTION, JOptionPane.ERROR_MESSAGE, null, null, null);
            return;
        }
        this.mainFrame.setCitySavedButtonEnable();
        this.mainFrame.getCurrentCityPanel().getCity1().setIsSaved(false);
        this.mainFrame.getCurrentCityPanel().getCurrentLocationListPanel().addNewRow(this.name, this.kindName, 0, this.quantity);
        this.mainFrame.setEnabled(true);
        this.dialog.dispose();
    }//GEN-LAST:event_okButtonActionPerformed

