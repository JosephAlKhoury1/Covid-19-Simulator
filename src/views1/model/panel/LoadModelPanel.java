    public LoadModelPanel(MainFrame frame, LoadModelDialog dialog) {
        this.frame = frame;
        this.dialog = dialog;
        list = ModelController.INSTANCE.selectAll();
        initComponents();
        if (list.size() == frame.getListModel().size()) {
            this.okButton.setEnabled(false);
            this.warningLabel.setVisible(true);
        } else {
            this.warningLabel.setVisible(false);
            int index = 0;
            for (Entry<Integer, String> e : list.entrySet()) {
                boolean exist = false;
                for (Model m : frame.getListModel()) {
                    if (m.getModelName().equals(e.getValue())) {
                        exist = true;
                    }
                }
                if (!exist) {
                    this.jComboBox1.addItem(e.getValue());
                    if (index == 0) {
                        index++;
                        this.id = e.getKey();
                    }
                }
            }
        }

    }

